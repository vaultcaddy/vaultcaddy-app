#!/bin/bash

# 将日文版 account.html 翻译为日文

FILE="jp/account.html"

echo "🔄 开始翻译日文版 account.html..."

# 1. HTML lang 属性
sed -i '' 's/<html lang="zh-TW">/<html lang="ja">/' "$FILE"

# 2. Title 和 meta
sed -i '' 's/帳戶設定/アカウント設定/' "$FILE"
sed -i '' 's/VaultCaddy明細書變換/VaultCaddy明細書変換/' "$FILE"

# 3. 导航栏
sed -i '' 's/>功能</>機能</' "$FILE"
sed -i '' 's/>價格</>価格</' "$FILE"
sed -i '' 's/>儀表板</>ダッシュボード</' "$FILE"
sed -i '' 's/>首頁</>ホーム</' "$FILE"

# 4. 用户菜单
sed -i '' 's/>帳戶</>アカウント</' "$FILE"
sed -i '' 's/>計費</>請求</' "$FILE"
sed -i '' 's/>登出</>ログアウト</' "$FILE"

# 5. 页面标题
sed -i '' 's/管理您的個人資料和帳戶偏好設定/プロフィールとアカウントの設定を管理/' "$FILE"

# 6. 个人资料部分
sed -i '' 's/>個人資料</>プロフィール</' "$FILE"
sed -i '' 's/目前計劃/現在のプラン/' "$FILE"
sed -i '' 's/升級計劃/プランをアップグレード/' "$FILE"
sed -i '' 's/管理訂閱/サブスクリプション管理/' "$FILE"
sed -i '' 's/下次計費/次回請求/' "$FILE"

# 7. Credits 部分
sed -i '' 's/Credits 使用情況/クレジット使用状況/' "$FILE"
sed -i '' 's/每處理 1 頁文檔消耗 1 個 Credit/1ページ処理ごとに1クレジット消費/' "$FILE"
sed -i '' 's/超額計費/超過分請求/' "$FILE"
sed -i '' 's/超出月度額度後自動按/月間上限を超えた場合、自動的に/' "$FILE"
sed -i '' 's/HKD \$0\.5\/頁/¥10\/ページ/' "$FILE"
sed -i '' 's/計費/で請求されます/' "$FILE"
sed -i '' 's/重置日期/リセット日/' "$FILE"
sed -i '' 's/購買 Credits/クレジット購入/' "$FILE"
sed -i '' 's/查看記錄/履歴を表示/' "$FILE"

# 8. 密码部分
sed -i '' 's/>密碼</>パスワード</' "$FILE"
sed -i '' 's/目前密碼/現在のパスワード/' "$FILE"
sed -i '' 's/新密碼/新しいパスワード/' "$FILE"
sed -i '' 's/密碼至少需要 8 個字元/パスワードは8文字以上必要です/' "$FILE"
sed -i '' 's/確認新密碼/新しいパスワードを確認/' "$FILE"
sed -i '' 's/更新密碼/パスワードを更新/' "$FILE"

# 9. 购买记录部分
sed -i '' 's/購買記錄/購入履歴/' "$FILE"
sed -i '' 's/所有記錄/全記録/' "$FILE"
sed -i '' 's/>日期</>日付</' "$FILE"
sed -i '' 's/>描述</>説明</' "$FILE"
sed -i '' 's/載入記錄中\.\.\./記録を読み込み中\.\.\./' "$FILE"

# 10. 危险区域
sed -i '' 's/危險區域/危険エリア/' "$FILE"
sed -i '' 's/刪除您的帳戶將永久移除所有資料，包括項目、文檔和設定。此操作無法復原。/アカウントを削除すると、プロジェクト、ファイル、設定を含むすべてのデータが完全に削除されます。この操作は元に戻せません。/' "$FILE"
sed -i '' 's/刪除帳戶/アカウントを削除/' "$FILE"

# 11. 创建项目模态框
sed -i '' 's/創建新項目/新しいプロジェクトを作成/' "$FILE"
sed -i '' 's/輸入項目名稱以創建新的文檔項目/プロジェクト名を入力して新しいドキュメントプロジェクトを作成/' "$FILE"
sed -i '' 's/項目名稱/プロジェクト名/' "$FILE"
sed -i '' 's/>取消</>キャンセル</' "$FILE"
sed -i '' 's/>創建</>作成</' "$FILE"

# 12. 隐私政策和服务条款
sed -i '' 's/隱私政策/プライバシーポリシー/' "$FILE"
sed -i '' 's/服務條款/利用規約/' "$FILE"

echo "✅ 翻译完成！"
echo ""
echo "验证关键翻译："
grep -n "アカウント設定\|プロフィール\|購入履歴\|クレジット使用" "$FILE" | head -5

